package pl.edu.pw.mini.po.beans;

public class JavaBeanLikeClass {

	private String name;
	private String lastName;
	private int age;
	private double bloodSugarContent;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public double getBloodSugarContent() {
		return bloodSugarContent;
	}

	public void setBloodSugarContent(double bloodSugarContent) {
		this.bloodSugarContent = bloodSugarContent;
	}

}
